<div class="bitem">
You also will help us find events of nest defense. <i>Nest defense</i> is defined as any behavior where the parent increases its probability of injury or mortality in order to increase the probability of the contents of the nest surviving.  Therefore, if a grouse is directing attacking a bird is nest defense, as well as when a bird may be doing broken wing displays or distraction displays.  Based on some preliminary video observations, sharp-tailed grouse appear to dip their heads, hold out their wings, and have their tails up in a similar fashion to displays observed at the lek (dancing grounds).
