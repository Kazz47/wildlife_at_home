function get_badges($user) {
    $badges = "";

    if ($user->bossa_total_credit + $user->bossa_credit_v2 > 2 * 60 * 60) {
        $badges .= "&nbsp;&nbsp;";
        $badges .= get_bossa_badge($user);
    }

    if ($user->wildlife_total_credit > 10000) {
        $badges .= "&nbsp;&nbsp;";
        $badges .= get_credit_badge($user);
    }

    return $badges;
}

